package ci.jsi.initialisation;

public class ValueSearch {

	String element;
	String value;
	
	public ValueSearch() {
		
	}

	public String getElement() {
		return element;
	}

	public void setElement(String element) {
		this.element = element;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	
	
}
